@dataclass
class TrainingPair:
    """A single training pair generated by the pulse engine."""
    input: str = ""
    output: str = ""
    domain_flags: list = field(default_factory=list)
    era_voice: str = "PRESENT_2026"
    entropy_bits: float = 0.0
    hash_signature: str = ""
    training_pair_id: str = ""
    source: str = "pulse_engine"
    model: str = "local"
    timestamp: str = ""

    def __post_init__(self):
        if not self.timestamp:
            self.timestamp = datetime.now(timezone.utc).isoformat()
        if not self.training_pair_id:
            self.training_pair_id = hashlib.sha256(
                f"{self.input}:{self.timestamp}".encode()
            ).hexdigest()[:16]
        if not self.hash_signature:
            raw = f"{self.output}:{self.entropy_bits}"
            self.hash_signature = hashlib.sha256(raw.encode()).hexdigest()[:16]

    def to_dict(self) -> dict:
        return asdict(self)

    def passes_gate(self, min_entropy: float = 4.2, max_entropy: float = 6.5) -> bool:
        return min_entropy <= self.entropy_bits <= max_entropy


def shannon_entropy(text: str) -> float:
    """Compute Shannon entropy of text in bits per character."""
    if not text:
        return 0.0
    freq = defaultdict(int)
    for ch in text:
        freq[ch] += 1
    n = len(text)
    return -sum((c / n) * math.log2(c / n) for c in freq.values())


def hash_sig(text: str) -> str:
    return hashlib.sha256(text.encode()).hexdigest()[:16]


