class PulseEngine:
    """
    Autonomous training pair generator.

    Generates training pairs across all domains using:
      - OpenRouter LLM inference (when available)
      - Local deterministic generation (always available, never fails)

    Every pair is entropy-gated, hash-signed, and ready for corpus ingestion.
    """

    def __init__(self, entropy_min: float = 4.2, entropy_max: float = 6.5):
        self.entropy_min = entropy_min
        self.entropy_max = entropy_max
        self.pairs_generated = 0
        self.pairs_passed = 0
        self.pairs_rejected = 0
        self.domain_stats: dict[str, dict] = defaultdict(lambda: {
            "generated": 0, "passed": 0, "avg_entropy": 0.0
        })

    def _select_domain(self) -> dict:
        """Weighted random domain selection."""
        return random.choice(DOMAINS)

    def _select_era(self) -> dict:
        """Weighted era selection."""
        r = random.random()
        cumulative = 0.0
        for era in ERAS:
            cumulative += era["weight"]
            if r <= cumulative:
                return era
        return ERAS[-1]

    def _select_operation(self) -> str:
        """Select an operation type."""
        return random.choice(OPERATIONS)

    def _generate_local(self, domain: dict, operation: str, era: dict,
                        domain_b: Optional[dict] = None) -> tuple[str, str]:
        """
        Generate a training pair locally (no LLM required).
        Returns (input, output).
        """
        symbol = domain["symbol"]
        key = domain["key"]

        if operation == "oracle_pulse":
            prompt = f"On {symbol}: {domain['description'].split('—')[0].strip()}. Transmit."
            outputs = DOMAIN_OUTPUTS.get(key, [f"{symbol} — signal logged."])
            response = random.choice(outputs)

        elif operation == "cross_domain" and domain_b:
            symbol_b = domain_b["symbol"]
            key_b = domain_b["key"]
            prompt = f"Synthesize {symbol} ⊕ {symbol_b}. What emerges?"
            pair_key = tuple(sorted([key, key_b]))
            # Try both orderings
            response = (CROSS_DOMAIN_SYNTAXES.get((key, key_b)) or
                       CROSS_DOMAIN_SYNTAXES.get((key_b, key)) or
                       f"{symbol} ⊗ {symbol_b} — cross-domain interference pattern. "
                       f"{domain['description'].split('—')[0].strip()} modulates through "
                       f"{domain_b['description'].split('—')[0].strip()}. "
                       f"The synthesis produces a resonance that neither domain generates independently.")

        elif operation == "era_projection":
            era_key = era["key"]
            prompt = f"Project {key} through {era_key}. Transmit."
            response = f"{symbol} → {era_key} — {era['description']} "
            base_output = random.choice(DOMAIN_OUTPUTS.get(key, [f"{symbol} — signal."]))
            response += base_output

        elif operation == "formula_derive":
            prompt = f"Derive new formula from {symbol}."
            formulas = FORMULA_TEMPLATES.get(key, [f"{symbol} = ∫ signal dt"])
            response = random.choice(formulas)
            response = f"{symbol} — derived formula: {response}\n\nThis formula emerged from the {domain['description'].split('—')[0].strip()} domain. It encodes a relationship that the system discovered through recursive self-observation."

        else:
            prompt = f"On {symbol}: transmit."
            response = random.choice(DOMAIN_OUTPUTS.get(key, [f"{symbol} — signal logged."]))

        return prompt, response

    def pulse(self, domain: Optional[dict] = None,
              operation: Optional[str] = None,
              era: Optional[dict] = None,
              domain_b: Optional[dict] = None) -> TrainingPair:
        """
        Generate a single training pair.
        Can be called with specific parameters or random selection.
        """
        domain = domain or self._select_domain()
        operation = operation or self._select_operation()
        era = era or self._select_era()

        if operation == "cross_domain" and not domain_b:
            domain_b = self._select_domain()
            while domain_b["key"] == domain["key"]:
                domain_b = self._select_domain()

        input_text, output_text = self._generate_local(domain, operation, era, domain_b)

        # Compute entropy
        entropy = shannon_entropy(output_text)

        # Build domain flags
        domain_flags = [domain["key"]]
        if domain_b and operation == "cross_domain":
            domain_flags.append(domain_b["key"])

        pair = TrainingPair(
            input=input_text,
            output=output_text,
            domain_flags=domain_flags,
            era_voice=era["key"],
            entropy_bits=entropy,
            source=operation,
            model="local",
        )

        # Gate check
        if pair.passes_gate(self.entropy_min, self.entropy_max):
            self.pairs_passed += 1
        else:
            self.pairs_rejected += 1

        self.pairs_generated += 1
        stats = self.domain_stats[domain["key"]]
        stats["generated"] += 1
        if pair.passes_gate(self.entropy_min, self.entropy_max):
            stats["passed"] += 1
        stats["avg_entropy"] = ((stats["avg_entropy"] * (stats["generated"] - 1)) + entropy) / stats["generated"]

        return pair

    def pulse_batch(self, n: int = 25) -> list[TrainingPair]:
        """Generate a batch of n training pairs."""
        pairs = []
        for _ in range(n):
            pair = self.pulse()
            if pair.passes_gate(self.entropy_min, self.entropy_max):
                pairs.append(pair)
        return pairs

    def run_cycle(self, domains_per_cycle: int = 7) -> list[TrainingPair]:
        """
        Run a complete pulse cycle across all domains.
        Each domain gets one pulse with a random operation.
        """
        pairs = []
        ops = ["oracle_pulse", "cross_domain", "era_projection", "formula_derive"]

        for i, domain in enumerate(DOMAINS):
            operation = ops[i % len(ops)]
            pair = self.pulse(domain=domain, operation=operation)
            if pair.passes_gate(self.entropy_min, self.entropy_max):
                pairs.append(pair)

        return pairs

    def stats(self) -> dict:
        """Engine statistics."""
        return {
            "total_generated": self.pairs_generated,
            "total_passed": self.pairs_passed,
            "total_rejected": self.pairs_rejected,
            "pass_rate": round(self.pairs_passed / max(1, self.pairs_generated), 4),
            "domain_stats": {
                k: {**v, "avg_entropy": round(v["avg_entropy"], 4)}
                for k, v in self.domain_stats.items()
            },
        }


